# Creating a String
String1 = 'Welcome to the Python'
print(String1)

# Creating a List
List = ['GeeksForGeeks']
print("\n")
print(List)

# Creating a List with the use of multiple values
List = ["Python", "For", "Programming"]
print("\nList containing multiple values:\n ")
print(List[0])
print(List[2])

# Creating a Tuple with nested tuples
Tuple1 = (0, 1, 2, 3)
Tuple2 = ('python', 'Program')
Tuple3 = (Tuple1, Tuple2)
print("\n\nTuple with nested tuples:\n ")
print(Tuple3)

# Creating a Set with a mixed type of values
set1 = set([1, 2, 'Python', 4, 'C Program', 6, 'C++'])
print("\nSet with the use of Mixed Values")
print(set1)

# Creating a Dictionary with Integer Keys
Dict = {1: 'Python', 2: 'For', 3: 'Working'}
print("\nDictionary with the use of Integer Keys: ")
print(Dict)
print(Dict.get(1))
print(Dict.get(2))
print(Dict.get(3))


